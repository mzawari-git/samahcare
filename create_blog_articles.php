foreach ($articles as $index => $articleData) {
    // Check if article already exists by title
    $existing = BlogPost::where('title_ar', $articleData['title_ar'])->first();
    if ($existing) {
        echo "[SKIP] Article #" . ($index + 1) . " already exists: {$articleData['title_ar']}\n";
        continue;
    }

    // Generate slug
    $slugBase = preg_replace('/[^\x{0600}-\x{06FF}\w\s]/u', '', $articleData['title_ar']);
    $slug = \Illuminate\Support\Str::slug($slugBase, '-') ?: 'post-' . ($index + 6);
    
    // Ensure unique slug
    $originalSlug = $slug;
    $counter = 1;
    while (BlogPost::where('slug', $slug)->exists()) {
        $slug = $originalSlug . '-' . $counter;
        $counter++;
    }

    $post = BlogPost::create([
        'title_ar' => $articleData['title_ar'],
        'slug' => $slug,
        'category' => $articleData['category'],
        'sort_order' => $articleData['sort_order'],
        'is_featured' => $articleData['is_featured'],
